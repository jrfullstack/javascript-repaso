// Los primitivos: es una información que no es un objetos y son inmutables

// - **Boolean**: true / false
// - **Null**: sin valor en lo absoluto
// - **Undefined**: una variable declarada que aun no se le asigna valor
// - **Number**: integers, floats, etc. / no hay diferencias entero o decimal
// - **String**: una cadena de caracteres
// - **Symbol**: es un valor único que no es igual a ningún otro valor